        <h2>premiere definition de GED:</h2>
        <p>La GED – ou Gestion Electronique des Documents – s’intéresse donc aux documents et aux données associées, et permet d’optimiser leur gestion et leur exploitation par des moyens électroniques spécialisés et performants.

            La GED est un procédé faisant intervenir des moyens électroniques – typiquement des logiciels et/ou des matériels – pour prendre en charge la gestion des documents, à savoir les opérations et actions destinées à traiter et à exploiter les documents, par exemple la capture, l’acquisition, la numérisation, la validation, la diffusion, le classement, l’indexation, l’archivage, Etc.</p>
    
