                <h2>Fonctionnalités de la GED: </h2>
                <p>Ainsi, un logiciel de GED dispose des multiples fonctionnalités pour gérer l’ensemble des étapes du cycle de vie d’un document numérique. Voici quelques-unes des fonctionnalités les plus répandus dans un logiciel de Gestion Documentaire :
                    * L'acquisition des documents<br>
                    * La gestion des documents<br>
                    * La recherche<br>
                    * le stockage <br>
        <strong style="color:red ;">acquisition des documents :</strong>  <br>
Acquérir des documents est la fonction première de la gestion électronique des documents. Qui comprend l’intégration des documents existants via leur numérisation grâce à des scanners, l’intégration de documents électroniques existants, la production de nouveaux documents électroniques.<br>
<strong style="color:red ;">Rechercher un fichier :</strong><br>
Dans la barre de recherche, entrez le champ que vous recherchez. La recherche s'effectue automatiquement et affiche vos résultats.<br>
<strong style="color:red ;">Stockage des données et des documents :</strong><br>
L’objectif étant de garantir l’intégralité des documents de l’entreprise, quels que soient leurs formats, données structurées et non structurées (PDF, document Word, photos etc.).<br>
<strong style="color:red ;">La gestion des documents</strong><br>
Les opérations de gestion englobent tout ce qui se passe après la création du document, c'est-à-dire :<br>
La sécurité et les droits d’accès : gestion des permissions<br>
L’administration proprement dite (classement, stockage)<br>
L’évolution du document : version et mise à jour<br>

